void sort(int size, struct student array[size]) {
	// your code goes here
  int x, y;
  struct student temp;
  
  for(x=0; x<size-1; x++) {
    for(y=0; y<size-x-1; y++) {
      if(strcmp(array[y].lname, array[y+1].lname) > 0) {
        temp = array[y];
        array[y] = array[y+1];
        array[y+1] = temp;
      }
    }
  } 
  
}


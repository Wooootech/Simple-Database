int search(int size, struct student array[size], char *lname) {
	// your code goes here
  int l=0;
  int r = size - 1;

  while(r >= l) {
    int mid = l + (r-l) /2;
    
    if(strcmp(array[mid].lname, lname) == 0) {
        return mid;
      }
    
    if(strcmp(array[mid].lname, lname) > 0) {
      r = mid + 1;
    } else {
      l = mid - 1;
    }
  }

  return -1;  
}

